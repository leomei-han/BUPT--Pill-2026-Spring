// ============================================================
//  pulse_sync  —  跨时钟域同步 + 边沿检测
//  将异步 1 Hz 慢时钟信号安全地转换为 10 kHz 主时钟域内的单周期脉冲
//  整个模块仅使用 3 个触发器，资源开销极小
// ============================================================
module pulse_sync(
    input  clk_10kHz,       // 10 kHz 主时钟（快时钟域）
    input  clk_1Hz,         // 1 Hz 外部时钟（慢时钟域，异步信号）
    output pill_pulse       // 输出：主时钟域内宽度为一个周期的脉冲
);

    // 3-bit 移位寄存器同步链
    // sr[0]: 第一级同步（可能处于亚稳态）
    // sr[1]: 第二级同步（亚稳态概率已极低，可安全使用）
    // sr[2]: 保存上一拍的值，用于和 sr[1] 比较做边沿检测
    reg [2:0] sr;

    always @(posedge clk_10kHz) begin
        sr <= {sr[1:0], clk_1Hz};   // 每个时钟沿整体右移一位
    end

    // 上升沿检测：sr[1]=1（当前值为高）且 sr[2]=0（上一拍为低）
    // 输出的脉冲宽度恰好是一个 10 kHz 时钟周期（100 μs）
    assign pill_pulse = (sr[2:1] == 2'b01);

endmodule
